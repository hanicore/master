# 🔄 While Loop


# 🟦 Example 1

i = 0

while i < 3:
    print("neow")
    i = i + 1


# 📦 i = 0
# Start the counter at 0.

# 🔄 while i < 3
# Repeat while i is less than 3.

# 🖨️ print("neow")
# Print "neow".

# ➕ i = i + 1
# Increase i by 1.

# 📈 Counter
# 0 → 1 → 2 → 3

# 📺 Output
# neow
# neow
# neow

# 💡 Remember
# while repeats code while the condition is True.
