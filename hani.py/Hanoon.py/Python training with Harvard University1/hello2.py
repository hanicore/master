# 🟦 Example 2

i = 3

while i != 0:
    print("meow")
    i = i - 1


# 📦 i = 3
# Start the counter at 3.

# 🔄 while i != 0
# Repeat while i is not 0.

# 🖨️ print("meow")
# Print "meow".

# ➖ i = i - 1
# Decrease i by 1.

# 📉 Counter
# 3 → 2 → 1 → 0

# 📺 Output
# meow
# meow
# meow

# 💡 Remember
# The loop stops when i becomes 0.
